/**
 * Function: add
 * Adds two integers.
 * Parameters:
 * - a: First integer
 * - b: Second integer
 * Returns: - Sum of the two integers
*/
int add(int a, int b) {
    return a + b;
}
/* Function: sub
   Subtracts two integers.
   Parameters:
     - a: First integer
     - b: Second integer
   Returns:
     - Difference of the two integers
*/
int sub(int a, int b) {
    return a - b;
}
/* Function: mul
   Multiplies two integers.
   Parameters:
     - a: First integer
     - b: Second integer
   Returns:
     - Product of the two integers
*/
int mul(int a, int b) {
    return a * b;
}
/* Function: div
   Divides two integers.
   Parameters:
     - a: First integer
     - b: Second integer
   Returns:
     - Quotient of the two integers
*/
int div(int a, int b) {
    return a / b;
}
/* Function: mod
   Computes the modulo of two integers.
   Parameters:
     - a: First integer
     - b: Second integer
   Returns:
     - Modulo of the two integers
*/
int mod(int a, int b) {
    return a % b;
}
